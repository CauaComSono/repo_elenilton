package App;

public class Funcionario {

    int cpf;
    String nome;
    int telefone;
    String funcao;

    public int cpf(){
        return cpf;
    }

    public String nome(){
        return nome;
    }

    public int telefone(){
        return telefone;
    }

    public String funcao(){
        return funcao;
    }


    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public void setFuncao(String funcao) {
        this.funcao = funcao;
    }
}
    
    

